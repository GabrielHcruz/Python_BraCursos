# Diferente de outras linguagens em Python o uso do IF é um pouco diferente.
# EX:

idade = int(input('Digite sua idade: '))

if idade > 18:
    print('Maior de idade')

elif idade > 16 and idade < 18:
    print('Infanto juvenil')

else:
    print('Menor idade')

